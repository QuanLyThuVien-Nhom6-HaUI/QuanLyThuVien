package haui.nhom6.qlthuvien.model;

public class PhieuMuon {
    private String maPhieu;
    private String maNhanVien;
    private String maNguoiDoc;
    private String ngayMuon;
    private String hanTraSach;

    public PhieuMuon() {}

    public String getMaPhieu() { return maPhieu; }
    public void setMaPhieu(String maPhieu) { this.maPhieu = maPhieu; }

    public String getMaNhanVien() { return maNhanVien; }
    public void setMaNhanVien(String maNhanVien) { this.maNhanVien = maNhanVien; }

    public String getMaNguoiDoc() { return maNguoiDoc; }
    public void setMaNguoiDoc(String maNguoiDoc) { this.maNguoiDoc = maNguoiDoc; }

    public String getNgayMuon() { return ngayMuon; }
    public void setNgayMuon(String ngayMuon) { this.ngayMuon = ngayMuon; }

    public String getHanTraSach() { return hanTraSach; }
    public void setHanTraSach(String hanTraSach) { this.hanTraSach = hanTraSach; }
}